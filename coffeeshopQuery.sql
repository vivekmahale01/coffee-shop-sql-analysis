-- 10. Show city name, total customers, total revenue, and population to identify the best city for opening a new outlet.

SELECT city.city_name,
       city.population,
       COUNT(DISTINCT customers.customer_id) AS total_customers,
       SUM(sales.total) AS total_revenue
FROM sales
JOIN customers
ON sales.customer_id = customers.customer_id
JOIN city
ON customers.city_id = city.city_id
GROUP BY city.city_name, city.population
ORDER BY total_revenue DESC;

-- 🔍 Business Problem
