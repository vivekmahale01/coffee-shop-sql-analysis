-- 3. How much revenue was generated from each city?

SELECT city.city_name,
       SUM(sales.total) AS total_revenue
FROM sales
JOIN customers
ON sales.customer_id = customers.customer_id
JOIN city
ON customers.city_id = city.city_id
GROUP BY city.city_name
ORDER BY total_revenue DESC;

-- 🔍 Business Problem
-- The company knows total revenue but does not know which cities are contributing the most revenue.
-- Without city-wise revenue analysis, management cannot identify high-performing markets.

-- 💡 Business Impact
-- Identifies top-performing cities
-- Helps prioritize expansion opportunities
-- Supports city-level marketing decisions
-- Reveals which locations generate the most revenue
---------------------------------------------------------------------------------------------------------------------------------------

